const RibbonButton = ({ 
  icon, 
  label, 
  onClick, 
  disabled = false, 
  className = '',
  iconType = 'svg' // 'svg' or 'text'
}) => {
  const IconComponent = iconMap[icon] || (() => <span className="text-lg">{icon}</span>);
  
  return (
    <Tooltip label={label} disabled={disabled}>
      <div
        className={`min-w-[72px] aspect-square bg-gray-100 rounded flex items-center justify-center hover:bg-blue-50 cursor-pointer transition-all duration-200 border border-transparent hover:border-gray-300 ${
          disabled ? 'opacity-50 cursor-not-allowed' : ''
        } ${className}`}
        onClick={disabled ? undefined : onClick}
      >
        {iconType === 'text' ? (
          <span className={`text-sm font-bold ${className}`}>{icon}</span>
        ) : (
          <IconComponent className="w-5 h-5 text-gray-700" />
        )}
      </div>
    </Tooltip>
  );
};

export default RibbonButton; 